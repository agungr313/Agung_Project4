package com.Agung.TProject4.model;

public class CastAndCrew {
    int imageUrl;
    String name;

    public CastAndCrew(int imageUrl, String name) {
        this.imageUrl = imageUrl;
        this.name = name;
    }

    public int getImageUrl() {
        return imageUrl;
    }

    public String getName() {
        return name;
    }
}
